package com.demo.shiroehcache.pojo;

import javax.persistence.*;

@Table(name = "t_role")
public class Role {
    @Id
    private Integer rid;

    private String name;

    /**
     * @return rid
     */
    public Integer getRid() {
        return rid;
    }

    /**
     * @param rid
     */
    public void setRid(Integer rid) {
        this.rid = rid;
    }

    /**
     * @return name
     */
    public String getName() {
        return name;
    }

    /**
     * @param name
     */
    public void setName(String name) {
        this.name = name == null ? null : name.trim();
    }
}